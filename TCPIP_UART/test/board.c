/*
*------------------------------------------------------------------------------
* void InitializeBoard(void)

* Summary	: This function configures all i/o pin directions
*
* Input		: None
*
* Output	: None
*
*------------------------------------------------------------------------------
*/
void BRD_init(void)
{
	
	// set all anolog channels as Digital I/O
	ADCON0 = 0x00;
	ADCON1 = 0x0F;
//	OSCCON |= 0X10; 
	OSCTUNE = 0x40;


	
	HEART_BEAT_DIRECTION = 0;
	
	SER_TX_DIR 		=  0;
	SER_RX_DIR 		=  1;


}
	


